package com.zetsubou_0.training.task.task007.been;

public class Animal {

    private final String name;

    private final int age;

    public Animal(final String name, final int age) {
        this.name = name;
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public int getAge() {
        return age;
    }
}
